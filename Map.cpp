Map::Map()
{
	track = TextureManager::LoadTexture("assets/track.png");
	road = TextureManager::LoadTexture("assets/road.png");
	house = TextureManager::LoadTexture("assets/house.png");

	LoadMap(lvl1);

	src.x = src.y = 0;
	src.w = dest.w = 32;
	src.h = dest.h = 32;

	dest.x = dest.y = 0;
}

Map::~Map()
{
	SDL_DestroyTexture(track);
	SDL_DestroyTexture(road);
	SDL_DestroyTexture(house);
}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			map[row][col] = arr[row][col];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			type = map[row][col];

			dest.x = col * 32;
			dest.y = row * 32;

			switch (type)
			{
			case 0:
				TextureManager::Draw(track, src, dest);
				break;
			case 1:
				TextureManager::Draw(road, src, dest);
				break;
			case 2:
				TextureManager::Draw(house, src, dest);
				break;
			default:
				break;
			}
		}
	}
}